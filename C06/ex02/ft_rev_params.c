#include <unistd.h>

int	main(int ag, char **av)
{
	int	i;
	int	j;

	i = ag - 1;
	while (i > 0)
	{
		j = 0;
		while (av[i][j])
		{
			write(1, &av[i][j], 1);
			j++;
		}
		write(1, "\n", 1);
		i--;
	}
	return (0);
}
